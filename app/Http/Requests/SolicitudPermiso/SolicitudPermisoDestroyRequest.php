<?php

namespace App\Http\Requests\SolicitudPermiso;

use App\Services\SolicitudPermiso\SolicitudPermisoStatusService;
use Illuminate\Foundation\Http\FormRequest;

class SolicitudPermisoDestroyRequest extends FormRequest
{
    /**
     * Determine if the user is authorized to make this request.
     */
    public function authorize(): bool
    {
        return $this->user()->hasPermissionTo('deleteWorkPermitRequests', 'web');
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array<string, mixed>
     */
    public function rules(): array
    {
        return [];
    }
} 